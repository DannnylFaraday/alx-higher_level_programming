#!/usr/bin/python3
def add(a, b):
    """This is an addition function

    Arguments:
        (a) is the first integer
        (b) is the second integer

    Returns:
        The return value. a + b
    """
    return (a + b)
def sub(a, b):
    """This is a subtractionfunction

    Arguments:
        (a) is the first integer
        (b) is the second integer

    Returns:
        The return value. a - b
    """
    return (a - b)

def multi(a, b):
    """This is a multiplication function

    Arguments:
        (a) is the first integer
        (b) is the second integer

    Returns:
        The return value. a * b
    """
    return (a * b)

def div(a, b):
    """This is a division function

    Arguments:
        (a) is the first integer
        (b) is the second integer

    Returns:
        The return value. a / b
    """
    return (a / b)
